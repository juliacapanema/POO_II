public class Ganho {

    private String tipoGanho;

    public int getMes() {
        return mes;
    }

    public void setMes(int mes) {
        this.mes = mes;
    }

    public int getAno() {
        return ano;
    }

    public void setAno(int ano) {
        this.ano = ano;
    }

    private int mes;

    private int ano;

    private double valorGanho;

    public String getTipoGanho() {
        return tipoGanho;
    }

    public void setTipoGanho(String tipoGanho) {
        this.tipoGanho = tipoGanho;
    }

    public double getValorGanho() {
        return valorGanho;
    }

    public void setValorGanho(double valorGanho) {
        this.valorGanho = valorGanho;
    }

}
